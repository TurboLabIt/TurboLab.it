    //<editor-fold defaultstate="collapsed" desc="*** 🧰 Helpers: services & HTTP ***">

    protected static function issueService() : Issue { return static::getService(Issue::class); }

    protected static function currentAuthor() : User { return static::getService(Factory::class)->getCurrentUserAsAuthor(); }


    protected function newIssueUrl() : string
    {
        return $this->generateUrl('app_forum_new_issue', [], UrlGeneratorInterface::ABSOLUTE_PATH);
    }


    /** A booted, NOT-logged-in client (mirrors loginAsSystem() without the login). */
    protected static function bootAnonymousClient() : void
    {
        static::ensureKernelShutdown();
        static::$client = static::createClient();
        static::$client->setServerParameter('HTTP_HOST', $_ENV['APP_SITE_DOMAIN']);
        static::$client->setServerParameter('HTTPS', 'https');
    }


    protected static function readForumIntegrationFile(string $eventFile) : string
    {
        static::ensureForumIntegrationFilesBuilt();
        $path = static::getProjectFilePath(self::EVENT_FILES_DIR . $eventFile);
        return (string)file_get_contents($path);
    }


    protected static function ensureForumIntegrationFilesBuilt() : void
    {
        if( self::$forumFilesBuilt ) {
            return;
        }

        // regenerate the phpBB integration files from the current Twig templates, so we assert on
        // what phpBB would actually serve right now (and not on a stale build)
        static::getService(ForumIntegrationBuilderCommand::class)->run(new ArrayInput([]), new NullOutput());
        self::$forumFilesBuilt = true;
    }
