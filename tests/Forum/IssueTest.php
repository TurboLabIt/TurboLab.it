/**
 * End-to-end tests for the "open a GitHub issue from a forum post" feature.
 * 📚 https://github.com/TurboLabIt/TurboLab.it/blob/main/docs/bug.md
 *
 * These are NOT unit tests: they run the real stack in dev — no mocks.
 *
 *  - Icon & modal visibility: rendered by *phpBB* at runtime from the integration files generated by
 *    ForumIntegrationBuilder. We (1) regenerate those files and assert the visibility rules baked
 *    into them, and (2) fetch a real, anonymous forum page over HTTP and assert the icon/modal are
 *    absent (the `S_USER_LOGGED_IN` gate actually hides them).
 *
 *  - Issue creation: driven through the Issue service exactly as ForumController does. The current
 *    user is established with loginAsSystem() (which sets the Symfony token that Factory reads). We
 *    drive the service rather than the HTTP endpoint on purpose: phpBBCookiesAuthenticator runs on
 *    the endpoint route and, finding no phpBB cookies, logs the session user straight back out — so
 *    an authenticated HTTP request would need a forged phpBB session. The controller is a thin
 *    wrapper, so the service-level tests cover its behaviour; the HTTP layer is still covered for
 *    the not-logged-in guard. The flow really calls GitHub::createIssue() (which returns its
 *    built-in test mock, issue "00-test", in non-prod) and really rewrites the phpBB post via the
 *    localhost-only special page public/special-pages/issues.php. We snapshot the target post and
 *    restore it afterwards, so the suite stays repeatable and doesn't pollute a real thread.
 */
class IssueTest extends BaseT
{
    private static ?\PDO $pdo = null;
    private static string $cmsDb   = '';
    private static string $forumDb = '';
    private static bool $forumFilesBuilt = false;

    private const string EVENT_FILES_DIR =
        'src/Forum/ext-turbolabit/forumintegration/styles/prosilver/template/event/';

    // phpBB classifies non-browser User-Agents as *bots*, and bots are backed by real phpBB user
    // accounts (so S_USER_LOGGED_IN is true for them and the modal renders). To get a genuine
    // anonymous-guest render we must impersonate a real browser.
    private const string BROWSER_UA =
        'Mozilla/5.0 (X11; Linux x86_64; rv:128.0) Gecko/20100101 Firefox/128.0';


    //<editor-fold defaultstate="collapsed" desc="*** 👁️ Icon & modal visibility (phpBB-rendered) ***">

    /**
     * The per-post bug icon is generated into the phpBB template with the right visibility rules:
     * logged-in only, in the TLI + Comments forums only, and never on a comment topic's first post.
     */
    public function testBugIconIsBuiltIntoTheForumTemplateWithTheExpectedVisibilityRules() : void
    {
        $html = static::readForumIntegrationFile('viewtopic_body_post_buttons_after.html');

        // the icon itself, wired to its post id (so each post opens an issue for *that* post)
        $this->assertStringContainsString('tli-open-issue-modal', $html);
        $this->assertStringContainsString('data-post-id="{postrow.POST_ID}"', $html);
        $this->assertStringContainsString('tli-bug-button-{postrow.POST_ID}', $html);

        // shown only to logged-in users...
        $this->assertStringContainsString('S_USER_LOGGED_IN', $html);

        // ...only in the allowed forums...
        $this->assertStringContainsString(
            '$TLI_ISSUE_FORUMS = [' . Forum::ID_TLI . ', ' . Forum::ID_COMMENTS . ']', $html
        );
        $this->assertStringContainsString('FORUM_ID in $TLI_ISSUE_FORUMS', $html);

        // ...and never on the auto-generated first post of a comments topic (that post IS the article)
        $this->assertStringContainsString(
            'not (FORUM_ID == ' . Forum::ID_COMMENTS . ' and postrow.S_FIRST_ROW)', $html
        );
    }


    /**
     * The confirmation modal is generated into the phpBB header, for logged-in users only, and its
     * "OK" button is wired to the Symfony endpoint that actually creates the issue.
     */
    public function testIssueModalIsBuiltIntoTheForumHeaderForLoggedInUsers() : void
    {
        $html = static::readForumIntegrationFile('overall_header_content_before.html');

        $this->assertStringContainsString('id="tli-issue-modal"', $html);
        $this->assertStringContainsString('tli-create-issue', $html);

        // the "OK" button posts to the new-issue endpoint
        $endpointPath = $this->generateUrl('app_forum_new_issue', [], UrlGeneratorInterface::ABSOLUTE_PATH);
        $this->assertStringContainsString('data-url="' . $endpointPath . '"', $html);

        // the modal exists only for logged-in users, in the allowed forums
        $this->assertStringContainsString('S_USER_LOGGED_IN', $html);
        $this->assertStringContainsString('FORUM_ID in $TLI_ISSUE_FORUMS', $html);
    }


    /**
     * On a real, live forum page an anonymous visitor gets neither the bug icon nor the modal:
     * the `S_USER_LOGGED_IN` gate is doing its job at phpBB runtime.
     */
    public function testAnonymousForumPageExposesNeitherIconNorModal() : void
    {
        $topicId = static::qualityTestCommentsTopicId();
        $this->assertGreaterThan(0, $topicId, 'The quality-test article has no comments topic to test against');

        $url = 'https://' . $_ENV['APP_SITE_DOMAIN'] . '/forum/viewtopic.php?t=' . $topicId;

        // a plain, cookie-less client => anonymous (self-signed cert on dev, like Issue::updatePost)
        $response = HttpClient::create()->request('GET', $url, [
            'verify_peer' => false, 'verify_host' => false, 'timeout' => 20,
            'headers'     => ['User-Agent' => self::BROWSER_UA],
        ]);

        $this->assertSame(Response::HTTP_OK, $response->getStatusCode(), "Failing forum URL: $url");

        $html = $response->getContent();

        // sanity: we really are looking at an anonymous-guest render (a login link, not a session)
        $this->assertStringContainsString('ucp.php?mode=login', $html, 'Expected an anonymous render with a login link');

        $this->assertStringNotContainsString('tli-open-issue-modal', $html, 'Anonymous users must not see the bug icon');
        $this->assertStringNotContainsString('id="tli-issue-modal"', $html, 'Anonymous users must not get the issue modal');
    }
