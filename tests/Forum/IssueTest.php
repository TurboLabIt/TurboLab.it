    /**
     * The "read the guide first" gate: with no recent visit to the guide article the request is
     * blocked; once a recent visit exists, it passes.
     */
    public function testTheGuideMustHaveBeenReadRecently() : void
    {
        static::loginAsSystem();
        $author = static::currentAuthor();
        $this->assertSame(User::ID_SYSTEM, $author->getId(), 'loginAsSystem() must establish the current user');

        try {
            // no recent visit => blocked
            static::clearSystemGuideVisits();
            try {
                static::issueService()->readGuideRequired($author);
                $this->fail('The guide gate should have blocked the request');
            } catch(AccessDeniedHttpException $ex) {
                $this->assertStringContainsStringIgnoringCase('guida', $ex->getMessage());
            }

            // a recent visit => allowed (no exception, fluent return)
            static::addSystemGuideVisit();
            $this->assertInstanceOf(Issue::class, static::issueService()->readGuideRequired($author));
        } finally {
            static::clearSystemGuideVisits();
        }
    }


    public function testPostsOutsideTheAllowedForumsAreRejected() : void
    {
        static::loginAsSystem();
        static::addSystemGuideVisit();      // get past the guide gate, so we reach the forum check
        static::clearSystemTestBugs();

        $author = static::currentAuthor();
        $postId = static::findNonAllowedForumPostId();
        $this->assertGreaterThan(0, $postId, 'No post found outside the allowed forums to test against');

        try {
            static::issueService()->createFromForumPostId($postId, $author, '127.0.0.1');
            $this->fail('A post outside the allowed forums should have been rejected');
        } catch(AccessDeniedHttpException $ex) {
            $this->assertStringContainsString('Forum not allowed', $ex->getMessage());
            $this->assertNull(static::fetchLatestSystemTestBug(), 'No issue must be created for a non-allowed forum');
        } finally {
            static::clearSystemTestBugs();
            static::clearSystemGuideVisits();
        }
    }


    public function testANonExistentPostIsRejected() : void
    {
        static::loginAsSystem();
        static::addSystemGuideVisit();
        $author = static::currentAuthor();

        try {
            static::issueService()->createFromForumPostId(999999999, $author, '127.0.0.1');
            $this->fail('A non-existent post should have been rejected');
        } catch(NotFoundHttpException $ex) {
            $this->assertStringContainsString('Post not found', $ex->getMessage());
        } finally {
            static::clearSystemGuideVisits();
        }
    }


    public function testAnInvalidPostIdIsRejected() : void
    {
        static::loginAsSystem();
        static::addSystemGuideVisit();
        $author = static::currentAuthor();

        try {
            static::issueService()->createFromForumPostId(0, $author, '127.0.0.1');
