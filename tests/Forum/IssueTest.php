    //<editor-fold defaultstate="collapsed" desc="*** 🧰 Helpers: DB (kernel-independent) ***">

    protected static function db() : \PDO
    {
        if( self::$pdo instanceof \PDO ) {
            return self::$pdo;
        }

        $url = parse_url($_ENV['DATABASE_URL'] ?? getenv('DATABASE_URL') ?: '');
        $dsn = sprintf('mysql:host=%s;port=%d;charset=utf8mb4', $url['host'] ?? '127.0.0.1', $url['port'] ?? 3306);

        self::$pdo = new \PDO($dsn, rawurldecode($url['user'] ?? ''), rawurldecode($url['pass'] ?? ''), [
            \PDO::ATTR_ERRMODE            => \PDO::ERRMODE_EXCEPTION,
            \PDO::ATTR_DEFAULT_FETCH_MODE => \PDO::FETCH_ASSOC,
        ]);

        self::$cmsDb   = ltrim($url['path'] ?? '', '/');
        self::$forumDb = $_ENV['APP_FORUM_DB_NAME'] ?? getenv('APP_FORUM_DB_NAME') ?: 'turbolab_it_forum';

        return self::$pdo;
    }


    protected static function cms() : string   { static::db(); return self::$cmsDb; }
    protected static function forum() : string { static::db(); return self::$forumDb; }


    /**
     * A visible, non-first reply in the quality-test article's comment thread: our sacrificial write
     * target. We snapshot it, let the feature rewrite it via the live special page, then restore it —
     * so the test really writes to the forum without ever touching a genuine discussion.
     */
    protected static function findWritableCommentPostId() : int
    {
        $forum   = static::forum();
        $topicId = static::qualityTestCommentsTopicId();

        $sql =
            "SELECT p.post_id
             FROM `$forum`.phpbb_posts p
             JOIN `$forum`.phpbb_topics t ON t.topic_id = p.topic_id
             WHERE p.topic_id = ? AND p.post_visibility = 1 AND p.post_delete_time = 0
               AND p.post_id <> t.topic_first_post_id
             ORDER BY p.post_id ASC LIMIT 1";

        $stmt = static::db()->prepare($sql);
        $stmt->execute([$topicId]);

        return (int)$stmt->fetchColumn();
    }


    /** A loadable post in a forum that is neither allowed (6/26) nor off-limit (so it yields 403, not 404). */
    protected static function findNonAllowedForumPostId() : int
    {
        $forum    = static::forum();
        $excluded = implode(',', array_map('intval', array_merge([Forum::ID_TLI, Forum::ID_COMMENTS], Forum::ID_OFFLIMIT)));
        $sql =
            "SELECT post_id
             FROM `$forum`.phpbb_posts
             WHERE forum_id NOT IN ($excluded) AND post_visibility = 1 AND post_delete_time = 0
             ORDER BY post_id ASC LIMIT 1";

        return (int)static::db()->query($sql)->fetchColumn();
    }


    /** The comments topic (forum ID_COMMENTS) attached to the quality-test article. */
    protected static function qualityTestCommentsTopicId() : int
    {
        $cms  = static::cms();
        $stmt = static::db()->prepare("SELECT comments_topic_id FROM `$cms`.article WHERE id = ?");
        $stmt->execute([Article::ID_QUALITY_TEST]);

        return (int)$stmt->fetchColumn();
    }


    protected static function fetchForumPost(int $postId) : array
    {
        $forum = static::forum();
        $stmt  = static::db()->prepare("SELECT * FROM `$forum`.phpbb_posts WHERE post_id = ?");
        $stmt->execute([$postId]);

        return $stmt->fetch() ?: [];
    }


    protected static function snapshotForumPost(int $postId) : array { return static::fetchForumPost($postId); }


    /** Put the post row back exactly as it was (every column but the PK), so the suite is repeatable. */
    protected static function restoreForumPost(array $row) : void
    {
        if( empty($row) ) {
            return;
        }

        $forum   = static::forum();
        $columns = array_filter(array_keys($row), fn($col) => $col !== 'post_id');
        $setSql  = implode(', ', array_map(fn($col) => "`$col` = :$col", $columns));

        $stmt = static::db()->prepare("UPDATE `$forum`.phpbb_posts SET $setSql WHERE post_id = :post_id");
        foreach($row as $col => $value) {
            $stmt->bindValue(":$col", $value);
        }
        $stmt->execute();
    }


    protected static function clearSystemGuideVisits() : void
    {
        $cms  = static::cms();
        $stmt = static::db()->prepare("DELETE FROM `$cms`.visit WHERE user_id = ? AND article_id = ?");
        $stmt->execute([User::ID_SYSTEM, Article::ID_ISSUE_REPORT]);
    }


    protected static function addSystemGuideVisit() : void
    {
        static::clearSystemGuideVisits();
        $cms  = static::cms();
        $stmt = static::db()->prepare(
            "INSERT INTO `$cms`.visit (article_id, user_id, ip_address, created_at, updated_at)
             VALUES (?, ?, ?, NOW(), NOW())"
        );
        $stmt->execute([Article::ID_ISSUE_REPORT, User::ID_SYSTEM, '127.0.0.1']);
    }


    /** Only ever touches the rows this test creates (the "00-test" mock issues for the System user). */
    protected static function clearSystemTestBugs() : void
    {
        $cms  = static::cms();
        $stmt = static::db()->prepare("DELETE FROM `$cms`.bug WHERE user_id = ? AND remote_id = '00-test'");
        $stmt->execute([User::ID_SYSTEM]);
    }


    protected static function fetchLatestSystemTestBug() : ?array
    {
        $cms  = static::cms();
        $stmt = static::db()->prepare(
            "SELECT * FROM `$cms`.bug WHERE user_id = ? AND remote_id = '00-test' ORDER BY id DESC LIMIT 1"
        );
        $stmt->execute([User::ID_SYSTEM]);

        return $stmt->fetch() ?: null;
    }
