    /** The endpoint itself rejects anonymous callers (loginRequired in ForumController). */
    public function testTheEndpointRequiresAuthentication() : void
    {
        static::bootAnonymousClient();

        static::$client->request('POST', $this->newIssueUrl(), ['postId' => static::findWritableCommentPostId()]);
        $response = static::$client->getResponse();

        $this->assertSame(Response::HTTP_FORBIDDEN, $response->getStatusCode());
        $this->assertStringContainsStringIgnoringCase('login', $response->getContent());
    }
