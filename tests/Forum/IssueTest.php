    /**
     * The full, real flow: a logged-in user who has read the guide opens an issue on a forum post.
     * GitHub::createIssue() runs (returning its test mock), a Bug row is persisted, and the forum
     * post is really edited (via the live special page) to link the new issue.
     */
    public function testOpeningAnIssueCreatesTheGithubIssueAndLinksItIntoTheForumPost() : void
    {
        static::loginAsSystem();
        static::addSystemGuideVisit();      // pass the read-guide gate
        static::clearSystemTestBugs();      // clean slate for the Bug assertions

        $author = static::currentAuthor();
        $postId = static::findWritableCommentPostId();
        $this->assertGreaterThan(0, $postId, 'No writable comment post found in forum ' . Forum::ID_COMMENTS);

        $snapshot = static::snapshotForumPost($postId);
        $this->assertNotEmpty($snapshot, 'Could not snapshot the target forum post');

        try {
            // mirror exactly what ForumController::newIssue() does
            $issue = static::issueService();
            $bug =
                $issue
                    ->readGuideRequired($author)
                    ->rateLimiting($author, '127.0.0.1')
                    ->createFromForumPostId($postId, $author, '127.0.0.1');

            // 1) GitHub::createIssue() ran (test mock) and the Bug is populated from its response
            $this->assertInstanceOf(Bug::class, $bug);
            $this->assertSame('00-test', $bug->getRemoteId());
            $this->assertStringContainsString('github.com', (string)$bug->getRemoteUrl());
            $this->assertStringContainsString('00-test', (string)$bug->getRemoteUrl());
            $this->assertSame($postId, $bug->getPost()->getId());
            $this->assertSame(User::ID_SYSTEM, $bug->getUser()->getId());

            // 2) the Bug row was persisted
            $dbBug = static::fetchLatestSystemTestBug();
            $this->assertNotNull($dbBug, 'No Bug row was persisted');
            $this->assertSame($postId, (int)$dbBug['post_id']);
            $this->assertNotEmpty($dbBug['user_ip_address']);

            // 3) updatePost() really rewrote the phpBB post (via the localhost-only special page)
            $responseText = $issue->updatePost($bug);
            $this->assertStringContainsString('updated', $responseText);

            $post = static::fetchForumPost($postId);
            $this->assertStringContainsString('00-test', $post['post_text']);
            $this->assertStringContainsString('su GitHub', $post['post_text']);
            $this->assertStringContainsString('github.com', $post['post_text']);
            $this->assertSame(User::ID_SYSTEM, (int)$post['post_edit_user']);
            $this->assertStringContainsString('Link to GitHub issue', (string)$post['post_edit_reason']);
        } finally {
            static::restoreForumPost($snapshot);
            static::clearSystemTestBugs();
            static::clearSystemGuideVisits();
        }
    }
